def area_rectangle(L,W):		#defining function for calculating area of rectangle
    area=L*W				#formula for area of rectangle
    return area				#returning the answer
    
def area_circle(r): 			#defining function for calculating area of circle
    area=3.14*(r**2) 			#formula for area of circle
    return area				#returning the answer

def area_triangle(B,H): 			#defining function for calculating area of triangle
    area=(1/2)*B*H			#formula for area of triangle
    return area				#returning the answer
